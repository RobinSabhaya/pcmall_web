import type { OrderStatus } from './OrderStatusTimeline.type';

export const getStatusColor = (status: OrderStatus, completed: boolean) => {
  if (!completed) return 'bg-light-300';

  switch (status) {
    case 'pending':
      return 'bg-orange';
    case 'confirmed':
      return 'bg-green';
    case 'processing':
      return 'bg-orange';
    case 'shipped':
      return 'bg-green';
    case 'delivered':
      return 'bg-radium';
    case 'cancelled':
      return 'bg-brand-primary';
    default:
      return 'bg-dark-500';
  }
};

